package Gun14;

public class  _03_forLoop {
    public static void main(String[] args) {
        // 0dan 10a kadar (10dahil)sayıları ekrana yazdırnız
        //10dan 0a (0 dahil) kadar sayıları ekrana yazdırınız
        
        int i=0;
        for(i=0; i<=10; i++)
            System.out.println("i = " + i);

        for (int j=10; j>=0; j--)
            System.out.println("j = " + j);    }
}
