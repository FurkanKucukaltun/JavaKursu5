package Gun08;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {
        int a=10;

        //a=a+5;
        a+=5; //ikisi aymı şey //a nın artış miktarı 5tir
        System.out.println("a = " + a);

        int b=10;
        //b=b-5;
        b-=5; // b nin azalış miktarı 5tir demek.
        System.out.println("b = " + b);

        int c=10;
        c/=5; // c nin bölüm değeri 5tir.
        System.out.println("c = " + c);

        int d=10;
        d*=5; //d=50
        System.out.println("d = " + d);

    }
}
